#include "3-calc.h"
/**
 * @a: first number
 * @b: second number
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * @a: first number
 * @b: second number
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* @a: first number
* @b: second number
* Return: int
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * @a: first number
 * @b: second number
 * Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * @a: first number
 * @b: second number
 * Return: int
 */
int op_mod(int a, int b)
{
	(a % b);
}
